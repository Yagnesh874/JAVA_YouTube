public class CWH_10_Operators {

    public static void main(String[] args) {
        int i = 56;
        System.out.println("Increment operator : ");
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        System.out.println("Decrement operator : ");

        int j = 10;
        System.out.println(j--);
        System.out.println(j);
        System.out.println(--j);

    }
}
